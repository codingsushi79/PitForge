  fujiClassic: "26.2",
  bahrainGp: "9.1",
  bahrainEndurance: "9.3",
  bahrainOuter: "9.2",
  bahrainPaddock: "9.5",
  sebringGp: "28.3",
  sebringSchool: "28.4",
  portimao: "60.1",
  imola: "25.5",
  cotaGp: "20.1",
  cotaNational: "20.2",
  interlagos: "1.2",
  lusail: "23.1",
